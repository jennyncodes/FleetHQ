-- View
CREATE OR REPLACE VIEW v_active_menu_items AS
SELECT
    m.MENU_ID,
    m.TRUCK_ID,
    m.MENU_NAME,
    m.IS_ACTIVE,
    d.DISH_ID,
    d.DISH_NAME,
    d.PRICE,
    d.IS_AVAILABLE
FROM MENU m
JOIN DISH d
    ON m.MENU_ID = d.MENU_ID
WHERE m.IS_ACTIVE = TRUE
  AND d.IS_AVAILABLE = TRUE;

-- Function
-- Recalculate an order total from its line items
CREATE OR REPLACE FUNCTION SP_REFRESH_ORDER_TOTAL(P_ORDER_ID INT)
RETURNS VOID
LANGUAGE plpgsql
AS $$
BEGIN
    UPDATE ORDERS o
    SET TOTAL_AMOUNT = COALESCE(
        (
            SELECT SUM(oi.COST)
            FROM ORDER_ITEM oi
            WHERE oi.ORDER_ID = P_ORDER_ID
        ),
        0.00
    )
    WHERE o.ORDER_ID = P_ORDER_ID;
END;
$$;

-- Function
-- Count upcoming events from today forward
CREATE OR REPLACE FUNCTION FN_UPCOMING_EVENT_COUNT()
RETURNS INT
LANGUAGE plpgsql
AS $$
DECLARE
    V_EVENT_COUNT INT;
BEGIN
    SELECT COUNT(*)
    INTO V_EVENT_COUNT
    FROM EVENT e
    WHERE e.EVENT_DATE >= CURRENT_DATE;

    RETURN V_EVENT_COUNT;
END;
$$;

-- Trigger function
-- Prevent overlapping schedule inserts
CREATE OR REPLACE FUNCTION TRG_BI_SCHEDULE_NO_OVERLAP_FN()
RETURNS TRIGGER
LANGUAGE plpgsql
AS $$
BEGIN
    IF EXISTS (
        SELECT 1
        FROM SCHEDULE s
        WHERE s.TRUCK_ID = NEW.TRUCK_ID
          AND s.SCHED_DATE = NEW.SCHED_DATE
          AND NEW.STARTTIME < s.ENDTIME
          AND NEW.ENDTIME > s.STARTTIME
    ) THEN
        RAISE EXCEPTION 'Schedule conflict: truck is already booked for an overlapping time slot.';
    END IF;

    RETURN NEW;
END;
$$;

-- Trigger function
-- Prevent overlapping schedule updates
CREATE OR REPLACE FUNCTION TRG_BU_SCHEDULE_NO_OVERLAP_FN()
RETURNS TRIGGER
LANGUAGE plpgsql
AS $$
BEGIN
    IF EXISTS (
        SELECT 1
        FROM SCHEDULE s
        WHERE s.TRUCK_ID = NEW.TRUCK_ID
          AND s.SCHED_DATE = NEW.SCHED_DATE
          AND s.SCHEDULE_ID <> OLD.SCHEDULE_ID
          AND NEW.STARTTIME < s.ENDTIME
          AND NEW.ENDTIME > s.STARTTIME
    ) THEN
        RAISE EXCEPTION 'Schedule conflict: truck is already booked for an overlapping time slot.';
    END IF;

    RETURN NEW;
END;
$$;

-- Trigger function
-- Validate order-item insert and compute cost
CREATE OR REPLACE FUNCTION TRG_BI_ORDER_ITEM_VALIDATE_AND_PRICE_FN()
RETURNS TRIGGER
LANGUAGE plpgsql
AS $$
DECLARE
    V_ORDER_TRUCK_ID INT;
    V_DISH_TRUCK_ID INT;
    V_DISH_PRICE DECIMAL(10,2);
BEGIN
    SELECT o.TRUCK_ID
    INTO V_ORDER_TRUCK_ID
    FROM ORDERS o
    WHERE o.ORDER_ID = NEW.ORDER_ID;

    SELECT m.TRUCK_ID, d.PRICE
    INTO V_DISH_TRUCK_ID, V_DISH_PRICE
    FROM DISH d
    JOIN MENU m
        ON d.MENU_ID = m.MENU_ID
    WHERE d.DISH_ID = NEW.DISH_ID;

    IF V_ORDER_TRUCK_ID IS NULL THEN
        RAISE EXCEPTION 'Invalid ORDER_ITEM: order does not exist.';
    END IF;

    IF V_DISH_TRUCK_ID IS NULL THEN
        RAISE EXCEPTION 'Invalid ORDER_ITEM: dish does not exist.';
    END IF;

    IF V_ORDER_TRUCK_ID <> V_DISH_TRUCK_ID THEN
        RAISE EXCEPTION 'Invalid ORDER_ITEM: dish must belong to the same truck as the order.';
    END IF;

    NEW.COST := NEW.QUANTITY * V_DISH_PRICE;
    RETURN NEW;
END;
$$;

