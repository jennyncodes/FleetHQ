-- Trigger function
-- Refresh order total after order-item insert
CREATE OR REPLACE FUNCTION TRG_AI_ORDER_ITEM_REFRESH_TOTAL_FN()
RETURNS TRIGGER
LANGUAGE plpgsql
AS $$
BEGIN
    PERFORM SP_REFRESH_ORDER_TOTAL(NEW.ORDER_ID);
    RETURN NULL;
END;
$$;

-- Trigger function
-- Refresh order total after order-item update
CREATE OR REPLACE FUNCTION TRG_AU_ORDER_ITEM_REFRESH_TOTAL_FN()
RETURNS TRIGGER
LANGUAGE plpgsql
AS $$
BEGIN
    PERFORM SP_REFRESH_ORDER_TOTAL(NEW.ORDER_ID);
    IF OLD.ORDER_ID <> NEW.ORDER_ID THEN
        PERFORM SP_REFRESH_ORDER_TOTAL(OLD.ORDER_ID);
    END IF;
    RETURN NULL;
END;
$$;

