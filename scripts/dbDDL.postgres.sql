-- PostgreSQL version of dbDDL.sql

-- Enum types
CREATE TYPE truck_status AS ENUM ('Active', 'Inactive');
CREATE TYPE order_status AS ENUM ('Pending', 'Completed', 'Cancelled');
CREATE TYPE menu_log_action AS ENUM ('INSERT', 'UPDATE', 'DELETE');

-- Core tables
CREATE TABLE FOOD_TRUCK (
    TRUCK_ID INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    TRUCK_NAME VARCHAR(50) NOT NULL,
    CUISINE_TYPE VARCHAR(30) NOT NULL,
    STATUS truck_status NOT NULL DEFAULT 'Active',
    LICENSE_PLATE CHAR(7) NOT NULL,
    CONSTRAINT UQ_FOOD_TRUCK_LICENSE UNIQUE (LICENSE_PLATE)
);

CREATE TABLE LOCATION (
    LOCATION_ID INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    LOCATION_ADDRESS VARCHAR(100) NOT NULL,
    ZONE_TYPE VARCHAR(20) NOT NULL,
    GPS_LAT DECIMAL(9,6) NOT NULL,
    GPS_LONG DECIMAL(9,6) NOT NULL,
    CONSTRAINT UQ_LOCATION_ADDRESS UNIQUE (LOCATION_ADDRESS),
    CONSTRAINT CHK_LOCATION_LAT CHECK (GPS_LAT BETWEEN 37.170000 AND 37.460000),
    CONSTRAINT CHK_LOCATION_LONG CHECK (GPS_LONG BETWEEN -122.040000 AND -121.760000)
);

CREATE TABLE CUSTOMER (
    CUSTOMER_ID INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    CUST_ADDRESS VARCHAR(100),
    EMAIL VARCHAR(100) NOT NULL,
    PHONE CHAR(10),
    CONSTRAINT UQ_CUSTOMER_EMAIL UNIQUE (EMAIL),
    CONSTRAINT CHK_CUSTOMER_PHONE CHECK (PHONE IS NULL OR PHONE ~ '^[0-9]{10}$')
);

CREATE TABLE EMPLOYEE (
    EMPLOYEE_ID INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    FNAME VARCHAR(30) NOT NULL,
    LNAME VARCHAR(30) NOT NULL,
    TRUCK_ID INT NULL,
    CONSTRAINT FK_EMPLOYEE_TRUCK
        FOREIGN KEY (TRUCK_ID) REFERENCES FOOD_TRUCK(TRUCK_ID)
        ON UPDATE CASCADE
        ON DELETE SET NULL
);

CREATE TABLE DRIVER (
    EMPLOYEE_ID INT PRIMARY KEY,
    LICENSE_NUM VARCHAR(20) NOT NULL,
    CONSTRAINT UQ_DRIVER_LICENSE UNIQUE (LICENSE_NUM),
    CONSTRAINT FK_DRIVER_EMPLOYEE
        FOREIGN KEY (EMPLOYEE_ID) REFERENCES EMPLOYEE(EMPLOYEE_ID)
        ON UPDATE CASCADE
        ON DELETE CASCADE
);

CREATE TABLE MANAGER (
    EMPLOYEE_ID INT PRIMARY KEY,
    CONSTRAINT FK_MANAGER_EMPLOYEE
        FOREIGN KEY (EMPLOYEE_ID) REFERENCES EMPLOYEE(EMPLOYEE_ID)
        ON UPDATE CASCADE
        ON DELETE CASCADE
);

CREATE TABLE SCHEDULE (
    SCHEDULE_ID INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    TRUCK_ID INT NOT NULL,
    LOCATION_ID INT NOT NULL,
    STARTTIME TIME NOT NULL,
    ENDTIME TIME NOT NULL,
    SCHED_DATE DATE NOT NULL,
    ASSIGNED_BY INT NULL,
    CONSTRAINT FK_SCHEDULE_TRUCK
        FOREIGN KEY (TRUCK_ID) REFERENCES FOOD_TRUCK(TRUCK_ID)
        ON UPDATE CASCADE
        ON DELETE CASCADE,
    CONSTRAINT FK_SCHEDULE_LOCATION
        FOREIGN KEY (LOCATION_ID) REFERENCES LOCATION(LOCATION_ID)
        ON UPDATE CASCADE
        ON DELETE CASCADE,
    CONSTRAINT FK_SCHEDULE_MANAGER
        FOREIGN KEY (ASSIGNED_BY) REFERENCES MANAGER(EMPLOYEE_ID)
        ON UPDATE CASCADE
        ON DELETE SET NULL,
    CONSTRAINT UQ_SCHEDULE_TRUCK_DATE_START UNIQUE (TRUCK_ID, SCHED_DATE, STARTTIME),
    CONSTRAINT CHK_SCHEDULE_TIME CHECK (STARTTIME < ENDTIME)
);

CREATE TABLE MENU (
    MENU_ID INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    TRUCK_ID INT NOT NULL,
    MENU_NAME VARCHAR(60) NOT NULL,
    IS_ACTIVE BOOLEAN NOT NULL DEFAULT TRUE,
    CONSTRAINT FK_MENU_TRUCK
        FOREIGN KEY (TRUCK_ID) REFERENCES FOOD_TRUCK(TRUCK_ID)
        ON UPDATE CASCADE
        ON DELETE CASCADE,
    CONSTRAINT UQ_MENU_TRUCK_NAME UNIQUE (TRUCK_ID, MENU_NAME)
);

CREATE TABLE DISH (
    DISH_ID INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    MENU_ID INT NOT NULL,
    DISH_NAME VARCHAR(60) NOT NULL,
    PRICE DECIMAL(10,2) NOT NULL,
    IS_AVAILABLE BOOLEAN NOT NULL DEFAULT TRUE,
    CONSTRAINT FK_DISH_MENU
        FOREIGN KEY (MENU_ID) REFERENCES MENU(MENU_ID)
        ON UPDATE CASCADE
        ON DELETE CASCADE,
    CONSTRAINT UQ_DISH_MENU_NAME UNIQUE (MENU_ID, DISH_NAME),
    CONSTRAINT CHK_DISH_PRICE CHECK (PRICE >= 0)
);

CREATE TABLE INGREDIENT (
    INGREDIENT_ID INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    INGREDIENT_NAME VARCHAR(40) NOT NULL,
    UNIT VARCHAR(10) NOT NULL,
    PER_COST DECIMAL(10,2) NOT NULL,
    CONSTRAINT UQ_INGREDIENT_NAME UNIQUE (INGREDIENT_NAME),
    CONSTRAINT CHK_INGREDIENT_COST CHECK (PER_COST >= 0)
);

CREATE TABLE DISH_INGREDIENT (
    DISH_ID INT NOT NULL,
    INGREDIENT_ID INT NOT NULL,
    REQUIRED_QUANTITY DECIMAL(10,2) NOT NULL,
    PRIMARY KEY (DISH_ID, INGREDIENT_ID),
    CONSTRAINT FK_DISH_INGREDIENT_DISH
        FOREIGN KEY (DISH_ID) REFERENCES DISH(DISH_ID)
        ON UPDATE CASCADE
        ON DELETE CASCADE,
    CONSTRAINT FK_DISH_INGREDIENT_INGREDIENT
        FOREIGN KEY (INGREDIENT_ID) REFERENCES INGREDIENT(INGREDIENT_ID)
        ON UPDATE CASCADE
        ON DELETE CASCADE,
    CONSTRAINT CHK_DISH_INGREDIENT_QTY CHECK (REQUIRED_QUANTITY > 0)
);

CREATE TABLE INVENTORY (
    INVENTORY_ID INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    TRUCK_ID INT NOT NULL,
    INGREDIENT_ID INT NOT NULL,
    QUANTITY DECIMAL(10,2) NOT NULL,
    CONSTRAINT FK_INVENTORY_TRUCK
        FOREIGN KEY (TRUCK_ID) REFERENCES FOOD_TRUCK(TRUCK_ID)
        ON UPDATE CASCADE
        ON DELETE CASCADE,
    CONSTRAINT FK_INVENTORY_INGREDIENT
        FOREIGN KEY (INGREDIENT_ID) REFERENCES INGREDIENT(INGREDIENT_ID)
        ON UPDATE CASCADE
        ON DELETE RESTRICT,
    CONSTRAINT UQ_INVENTORY_TRUCK_INGREDIENT UNIQUE (TRUCK_ID, INGREDIENT_ID),
    CONSTRAINT CHK_INVENTORY_QTY CHECK (QUANTITY >= 0)
);

CREATE TABLE EVENT (
    EVENT_ID INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    EVENT_NAME VARCHAR(60) NOT NULL,
    EVENT_DATE DATE NOT NULL,
    EVENT_LOCATION INT NOT NULL,
    CONSTRAINT FK_EVENT_LOCATION
        FOREIGN KEY (EVENT_LOCATION) REFERENCES LOCATION(LOCATION_ID)
        ON UPDATE CASCADE
        ON DELETE RESTRICT
);

CREATE TABLE EVENT_BOOKING (
    EVENT_ID INT NOT NULL,
    TRUCK_ID INT NOT NULL,
    PRIMARY KEY (EVENT_ID, TRUCK_ID),
    CONSTRAINT FK_EVENT_BOOKING_EVENT
        FOREIGN KEY (EVENT_ID) REFERENCES EVENT(EVENT_ID)
        ON UPDATE CASCADE
        ON DELETE CASCADE,
    CONSTRAINT FK_EVENT_BOOKING_TRUCK
        FOREIGN KEY (TRUCK_ID) REFERENCES FOOD_TRUCK(TRUCK_ID)
        ON UPDATE CASCADE
        ON DELETE CASCADE
);

CREATE TABLE ORDERS (
    ORDER_ID INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    CUSTOMER_ID INT NOT NULL,
    TRUCK_ID INT NOT NULL,
    ORDER_DATE DATE NOT NULL,
    TOTAL_AMOUNT DECIMAL(10,2) NOT NULL DEFAULT 0.00,
    STATUS order_status NOT NULL,
    CONSTRAINT FK_ORDERS_CUSTOMER
        FOREIGN KEY (CUSTOMER_ID) REFERENCES CUSTOMER(CUSTOMER_ID)
        ON UPDATE CASCADE
        ON DELETE RESTRICT,
    CONSTRAINT FK_ORDERS_TRUCK
        FOREIGN KEY (TRUCK_ID) REFERENCES FOOD_TRUCK(TRUCK_ID)
        ON UPDATE CASCADE
        ON DELETE RESTRICT,
    CONSTRAINT CHK_ORDERS_TOTAL CHECK (TOTAL_AMOUNT >= 0)
);

CREATE TABLE ORDER_ITEM (
    ORDER_ID INT NOT NULL,
    DISH_ID INT NOT NULL,
    QUANTITY INT NOT NULL,
    COST DECIMAL(10,2) NOT NULL DEFAULT 0.00,
    PRIMARY KEY (ORDER_ID, DISH_ID),
    CONSTRAINT FK_ORDER_ITEM_ORDER
        FOREIGN KEY (ORDER_ID) REFERENCES ORDERS(ORDER_ID)
        ON UPDATE CASCADE
        ON DELETE CASCADE,
    CONSTRAINT FK_ORDER_ITEM_DISH
        FOREIGN KEY (DISH_ID) REFERENCES DISH(DISH_ID)
        ON UPDATE CASCADE
        ON DELETE RESTRICT,
    CONSTRAINT CHK_ORDER_ITEM_QTY CHECK (QUANTITY > 0),
    CONSTRAINT CHK_ORDER_ITEM_COST CHECK (COST >= 0)
);

